/**
 * 送料を計算する
 * @param prefecture 都道府県名（例: "東京都"）
 * @param subtotalJpy 商品小計
 * @returns 送料（円）
 */
export function calcShippingFee(prefecture: string, subtotalJpy: number): number {
  // 1万円以上で送料無料
  if (subtotalJpy >= 10000) return 0;
  return SHIPPING_FEE_BY_PREFECTURE[prefecture] ?? 1500;
}

